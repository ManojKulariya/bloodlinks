<h3>Step 1:The Donation </h3>

<p>You have visited  blood donation center to donate blood. You are asked to fill out a health history form and a small physical. The blood is collected in several test tubes, as well as a pint of blood. Your donation, test tubes, and donor record are all marked with the same bar code. You are asked to stay after your donation to complete a questionnaire. Your blood is taken in advance of being transported to the respective blood center for processing. Test tubes containing your blood are sent to the lab.</p>


<h3>Step 2: Processing of Blood</h3>

<p>Your donation data is processed through a computer at our processing centre. The separated transfusable components are platelets, plasma, and red cells, in addition to whole blood donations.
Cryoprecipitate, a plasma component, helps control the risk of bleeding by assisting blood clotting. Red cells and platelets are processed to leuko-reduce, which reduces the possibility of the recipient developing allergic reactions to the transfusion.</p>

<h3>Step 3: Testing Process</h3>
<p>The Sample Blood arrives at the Testing Laboratory where requisite test has been performed to identify the type of blood or any disease present in it.
Your donation will be discarded if the test results are positive (it is shared only to the donors). Test results are transferred electronically to the processing center within 24 hours.
</p>


<h3>Step:4 Storing of Donated Blood</h3>
<p>Upon the arrival of test results, units suitable for transfusion are labeled and stored. Red cells are kept at 6ºC in refrigerators for up to 42 days. Platelets are stored at room temperature in agitators for up to five days. Plasma and cryo are frozen and kept in freezers for up to one year.</p>

<h3>Step 5: Blood Distribution</h3>
<p>Blood is available 24/7 to all the hospitals and users whenever they raise a request of Blood, there is a prompt delivery to the requisite hospitals/ users.</p>

<h3>Step 6: Transfusion of Blood</h3>
<p>When an individual is stricken with an infection or injury, they are admitted to a hospital or treatment center. Physicians determine whether a transfusion is required and what kind. Blood is given to patients in a wide range of circumstances, including serious accidents (such as in a car crash), surgeries, childbearing, anemia, blood disorders, cancer treatment, and many others.</p>


